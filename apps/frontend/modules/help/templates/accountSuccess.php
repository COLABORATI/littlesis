<a name="levels" class="help-anchor">text</a>
<div class="help-article-header">Understanding Permission Levels</div>
<div class="help-article">		
All data on LittleSis is publicly viewable, but only registered users—called analysts—can make changes to data on the site.  In order to uphold <strong><a href="/about">our mission</a></strong> and the accuracy of the site’s data, we have two different levels that include the following permissions.
<hr>
<p><strong>Beginner Analyst</strong>
<ul>
<li>Add new people and organizations (contributor)
<li>Add relationships between existing entities
<li>Edit existing profiles, lists, relationships and sources (editor)
<li>Write notes to share your work with other analysts
<li>Participate in research groups
</ul>
<strong>Advanced Analyst</strong>
<ul>
<li>All Beginner permissions
<li>Create new lists (lister)
<li>Match donation data from OpenSecrets 
<li>Add and edit contact info (contacter)
<li>Use the expert research tools to analyze complex networks
<li>Remove profiles, relationships, and lists (deleter)
<li>Add new relationship and list data in bulk (importer)
<li>Use the LittleSis bookmarklet to add relationships you find while browsing the web
</ul>
The LittleSis staff have a few other administration powers, like blocking malicious users, moving analysts to different levels, and creating new groups.</div>
