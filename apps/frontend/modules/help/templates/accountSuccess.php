<div class="help-article-header">Understanding Networks</div>
<div class="help-article">
Every person and organization profiled in LittleSis belongs to at least one geographic power network.  That network is automatically assigned according to the default network of the analyst who created the entity. <strong>All new analysts are assigned United States as their default network.</strong>  
<hr>
You can easily change a person or organization's network by <strong><a href="/help/beginnerProfiles#editing-profile">editing their profile</a></strong>.  If you want to add a lot of people and organizations that belong in a different network than your default, change it:
<ol class="num-list">
<li>Click your username tab to go to your dashboard.
<li>Click <strong>Settings</strong> in the header.
<li>Click <strong>Edit</strong>
<li>Select a new network from the <strong>Default Network</strong> drop-down menu. 
</ol>
<strong><a href="/contact">Contact us</a></strong> to create a new network.</div>
