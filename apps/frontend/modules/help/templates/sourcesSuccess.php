<a name="q-appropriate" class="help-anchor">text</a>
<div class="help-article-header">What are appropriate sources for the information I add to LittleSis?</div>
<hr>
<div class="help-article">
When you’re referencing data to a particular source, use <strong>original sources only</strong>.  Wikipedia and similar websites, like Little Sis, aggregate and organize information and asks users to reference the data they enter with original sources (on LittleSis references are required!) You can think of any interesting info you find on these sites as “leads" that you simply need to back-up with an original source before adding it to LittleSis.  Usually the references sections of Wikipedia articles can help you follow your leads or a simple Google search.  
<p>You can also <strong><a href="#source-link">add an interesting Wikipedia page to a profile’s Source Links</a></strong> as a general reference, which is different from using it as a source to support new data you add.</p>
