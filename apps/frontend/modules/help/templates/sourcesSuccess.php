<div class="help-toc-page">Sources</div>
<div class="help-article"> It's our strict policy to limit information on LittleSis to facts that can be supported by journalistic, academic, or government documents. Inaccurate, firsthand, anecdotal, or editorial content will be removed by the site staff or by other users. <strong>All data entered into LittleSis, by a user or by a bot, must include a reference</strong>. 
<p>On all data pages you'll see a <strong>Source Links</strong> section.  This is a bibliographic pointer to the resources that document raw information on the site.</p>
