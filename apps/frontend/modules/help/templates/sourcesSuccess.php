<div class="help-article-header">Adding a New Source Link</div>
<div class="help-article">
If you want to add an interesting article or website to an entity--not as a reference for new data-- you can add a new source link. 
<hr>
<ol class="num-list">
<li>Go to the profile, relationship or list to which you want to add a new source.
<img src="<?php echo image_path('system/help/source-link.png') ?>" class="help-centered-image">
<li>Click <strong>Add</strong> in the Source Links section.
<li>Copy the URL of the web page you want to include as a source.
<li>Paste in the <strong>Source URL</strong> box.
<li>Enter a <strong>Display name</strong> that’s detailed enough to allow others to identify your source. 
<li>You can also click More to add optional details about your source
<li>Click <strong>Add</strong> to finish.
</ol>
