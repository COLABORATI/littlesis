<a name="editing-source" class="help-anchor">text</a>
<div class="help-article-header">Editing a Source</div>
<div class="help-article">
Keep in mind that sources mainly exist to support data on LittleSis.  Don’t edit any source in a way that will compromise its ability to do that! 
<hr>
<ol class="num-list">
<li>On the page where you want to edit a source, click <strong>Edit</strong> next to Source Links.
<li>Click <strong>Edit</strong> again next to the specific source you want to change.
<li>After making changes, click <strong>Save</strong> to finish.
</ol>
