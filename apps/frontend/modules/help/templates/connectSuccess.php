<a name="writing-note" class="help-anchor">text</a>
<div class="help-article-header"> Writing a Note <hr></div>
<div class="help-article">
<ol class="num-list">
<li>Click your username tab to go to your dashboard.
<li>Click <strong>Write a note</strong>.
<li>Mention other analyst(s) using <strong>@username</strong> and your note will be posted on their <strong><a href="#managing-note">dashboard(s)</a></strong>.
<li>Link to a LittleSis entity and the note will be posted on its page:
<ul>
<li><strong><a href="#entity-note">Profiles</a></strong>.
<li><strong><a href="#rel-list-note">Relationships or lists</a></strong>.
<li><strong><a href="#group-note">Research groups</a></strong>.
</ul>
<li>Check <strong>Private</strong> to make the note only viewable by yourself and any analysts you mention.
<li>Click <strong>Post</strong> to finish.
</ol></div>
<div class="help-top"><a href="#top">^back to top</a></div> 

<a name="entity-note" class="help-anchor">text</a>
<div class="help-article-header">Linking to a Profile in a Note<hr></div>
<div class="help-article">
<ol class="num-list">
<li>In the <strong>Find an entity</strong> box, type in the person or organization you want to create a link to. 
<li>Click the correct name in the drop-down.
<li>Click <strong>Copy</strong> to add it in mention format to your note.    
<li>The “link name" in brackets indicates how the mention will be displayed in your note.  To change, type a different name between the brackets.
<li>Check <strong>Private</strong> to make the note only viewable by yourself and anyone you mention.
<li>Click <strong>Post</strong> to finish.
</ol></div>
<div class="help-top"><a href="#top">^back to top</a></div> 

<a name="rel-list-note" class="help-anchor">text</a>
<div class="help-article-header">Linking to a Relationship or List in a Note<hr></div>
<div class="help-article">
<ol class="num-list">
<li>Search for the person, organization or list you want to write a note about.
<ul>
<li>For lists, click its name in the search results.
<li>For relationships, click the person or org's name with the relationship you want to link to and click the <strong>relationship title</strong> on their profile page.
</ul>
<li>Find the <strong>Notes</strong> section of the list/relationship page to see all the notes mentioning this entity.
<li>Click <strong>Write a note</strong> in the Notes section to link to this list/relationship in a new note. 
<li>Click <strong>Note all</strong> in the Notes Section to link to this list/relationship and any analysts who’ve edited this entity in a new note.  
<li>Check <strong>Private</strong> to make the note only viewable by yourself and anyone you mention.
<li>Click <strong>Post</strong> to finish.
</ol></div>
<div class="help-top"><a href="#top">^back to top</a></div> 

<a name="managing-note" class="help-anchor">text</a>
<div class="help-article-header">Managing My Notes<hr></div>
<div class="help-article">
<ol class="num-list">
<li>Click your username tab to go to your dashboard.
<li>Your dashboard displays all the notes you’ve written and all the notes others have written mentioning you.
<li>To show only notes you’ve written, click <strong>Hide replies</strong>.
<li>Notes can’t be edited once they’ve been posted. To delete a note you’ve written, click the trash can on the right side of the note. 
<li>Notes marked with a padlock are private to you and any other analysts mentioned.     
</ol></div>
