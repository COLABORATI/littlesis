<p><b>MORE ABOUT LITTLE SISTER</b></p>

<p>At its core, Little Sister offers interlinked profiles of powerful people and organizations in the public and private sectors. Profiles detail a wealth of information vital to any investigation of the ways power and money guide the formulation of public policy, from board memberships to campaign contributions, old school ties to government contracts. Little Sister integrates and cross-references a wide range of data currently scattered across print and electronic resources that are disconnected and often difficult to find.</p>

<p>In the future, in addition to a rich catalog of profiles, Little Sister will provide users with sophisticated tools for searching, exploring, and analyzing the network of data at their disposal. Little Sister will allow users to map social networks, meticulously follow the money, and highlight relationships overlooked in breaking news stories.</p>

<p>Core data sets will be maintained by the site's developers, but most fields in Little Sister's database will be open to contribution and editing, while keeping the process of revision transparent and accountable. The overall breadth and detail of Little Sister's data will rely on an active and collaborative user community.</p>


<p><b>MORE ABOUT PAI</b></p>

<p>The Public Accountability Initiative (PAI), a 501(c)3 nonprofit, is a nonpartisan research and educational organization focused on corporate and government accountability. Our mission is to facilitate and produce investigative research that supports strategic community action and empowers citizens to hold their leaders accountable.</p>

<p>PAI was formed in early 2008 by a group of academics, public interest lawyers, researchers, and web developers affiliated with leading universities and major social change organizations. In previous years, members of PAI's staff and board have collaborated on high-profile campaigns and generated original accountability research that has appeared on the front pages of the Wall Street Journal, the Boston Globe, and other major media outlets.</p>